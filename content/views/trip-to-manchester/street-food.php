<h3>Rollers Café</h3>
<?php echo \Images::create('left-floating-image', 'eat-to-treat.png', null, null); ?>
<p>
Rollers Café is the place where you can buy tasty custom-made pizzas and sweet gooey cinnamon rolls under the same roof. I was initially tempted by their combo-meal offer which consisted of one slice of pizza, one roll and a drink and therefore decided to give it a go.
</p>
<h4>Choosing toppings for your pizza</h4>
<p>
For your pizza, you get to decide which toppings you would like on top. For my first pizza, I think I chose (although I can't remember off the top of my head) tomato-base, chicken, olives and peppers. Of course, you also get some delicious Italian mozzerella cheese to make it a pizza and not something else.
</p>
<h4>Selecting a cinammon roll</h4>
<p>
Now moving onto the cinammon roll, there are several varieties of this. You can have one with bits of chocolate that naturally melt from the heat. You can also have one sprinkled with nuts on top or if you prefer simplicity then you can also have it plain. I chose the one with the bits of chocolate. The amount of vanilla sauce is pretty generous and if you decide to use your hands then you'll be licking each and every finger clean just like I did.
</p>
