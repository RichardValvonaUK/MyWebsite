<h2>Street Food in Manchester</h2>
<p>
Living in Manchester is a treat when it comes to eating out. As someone who doesn't like spending too much money, I found myself more willing to treat myself to the street-food once or twice a week as it's quite affordable, especially when you're away from the city-centre. The quality of the food is genuinely very good and I would like to mention a few places which I would like to recommend to anyone visiting Manchester.
</p>

<h3>Eat 2 Treat</h3>
<?php echo \Images::create('right-floating-image', 'eat-to-treat.png', null, null); ?>
<p>
In the city centre, there are many kebab shops to choose from. Out of all of the places I've tried, this is definitely my favourite. You'll find this kebab place on the corner of the Food Market in the <?php echo URLS::absLink('Manchester Arndale, the main shopping centre', 'https://www.manchesterarndale.com/', null); ?> only a five minute walk away from <?php echo URLS::absLink('Piccadilly Gardens', 'http://www.visitmanchester.com/articles/piccadilly-gardens/', null); ?> where you'll find all of the local buses.
</p>
<p>
The first time I was here, I was greeted by the owner who was pleasant and made me feel welcome. I didn't buy from this place initially, but after thinking about it, I felt bad and decided to return on a different day and try out one of his kebabs. I went for the double lamb shish kebab which cost only £3. It was of a decent size and had salad in it. There was that taste of fresh herbs like they'd been picked on the same morning and the meat he used was of very high quality and likely came from a good source. Best of all, it filled me up.
</p>
<p>
I ended up going back there at least a couple more times during my stay in Manchester and hope that he's still there if I ever choose to go back again. As well as his kebabs, try out his chicken curry with rice. He gave me a sample to try on the final time I was there and was bowled over by the recipe he used. Please try it at least once. You will not be disappointed.
</p>


